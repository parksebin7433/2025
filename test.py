# 퍼스널컬러 선택
personal = st.selectbox("✨ 퍼스널컬러를 선택해주세요:", list(personal_palette.keys()))

# 날씨 조건 선택
weather = st.selectbox("🌤️ 오늘의 날씨는?:", ["맑음", "흐림", "비", "눈", "덥다", "선선하다", "춥다"])

# 아이템 추가 여부
extra_items = st.multiselect("👜 추가 아이템을 선택해주세요:", ["모자", "액세서리", "아우터"])

if st.button("✨ 코디 추천받기"):
    # 퍼스널컬러 가이드 메시지
    if selected_top not in personal_palette[personal]:
        st.warning(f"선택한 상의 색({selected_top})은 {personal}과 잘 맞지 않을 수 있어요. 추천 상의: {', '.join(personal_palette[personal])}")

    # 팔레트 찾기
    palette = outfit_palette[season][style].get(selected_top, style_defaults[style])

    st.subheader(f"{season} · {style} · {personal} 추천 코디 3세트 ✨")
    for i in range(1, 4):
        bottom = random.choice(palette["하의"])
        shoes = random.choice(palette["신발"])
        bag = random.choice(palette["가방"])

        st.markdown(f"### 👗 코디 {i}")
        color_box("👕 상의", selected_top)
        color_box("👖 하의", bottom)
        color_box("👟 신발", shoes)
        color_box("👜 가방", bag)

        # 날씨 조건 반영 메시지
        st.write(f"☁️ 오늘 날씨({weather})에 맞는 코디입니다!")

        # 추가 아이템 출력
        if extra_items:
            st.write("➕ 추가 아이템:", ", ".join(extra_items))

        st.markdown("---")

    st.info("👉 계절·스타일·퍼스널컬러·날씨까지 반영된 오늘의 코디 완성!")
